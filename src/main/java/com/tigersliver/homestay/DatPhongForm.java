    private void datPhong() {
        try {
            // Lấy dữ liệu khách hàng từ JTextField
            String maKH = txtMaKH1.getText();
            String hoTen = txtTenKH.getText();
            String soDienThoai = txtSDT.getText();
            String cccd = txtCCCD.getText();
            String gioiTinh;

            if (rdoNam.isSelected()) {
                gioiTinh = "Nam";
            } else if (rdoNu.isSelected()) {
                gioiTinh = "Nữ";
            } else {
                gioiTinh = "Chưa chọn";
            }

            // Lưu khách hàng
            KhachHang kh = new KhachHang();
            kh.setMaKH(maKH);
            kh.setHoTen(hoTen);
            kh.setSoDienThoai(soDienThoai);
            kh.setSoCCCD(cccd);
            kh.setGioiTinh(gioiTinh);

            KhachHangDAO khDAO = new KhachHangDAO();
            khDAO.insert(kh);

            // Lấy dữ liệu đặt phòng từ JTextField
            String maDP = "DP" + System.currentTimeMillis(); // Tạo mã đặt phòng tự động
            String ngayDiText = txtNgayDi.getText(); // Lấy giá trị từ JTextField
            LocalDate ngayDi = null;
            if (!ngayDiText.isEmpty()) {
                ngayDi = LocalDate.parse(ngayDiText); // Chỉ chuyển đổi nếu có giá trị
            }
            String maNV = txtMaNV.getText();
            String maPhong = lblMaPhong.getText();
            String maKieuThue = cboKieuThue.getSelectedItem().toString();

            // Lưu đặt phòng
            DatPhong dp = new DatPhong();
            dp.setMaDP(maDP);
            // Không cần set NgayDen vì SQL Server sẽ tự động điền giá trị mặc định
            dp.setNgayDi(ngayDi != null ? Date.valueOf(ngayDi) : null); // Nếu không nhập, để trống
            dp.setMaNV(maNV);
            dp.setMaKH(maKH);
            dp.setMaPhong(maPhong);
            dp.setMaKieuThue(maKieuThue);

            DatPhongDAO dpDAO = new DatPhongDAO();
            dpDAO.insert(dp);

            // Lưu dịch vụ đã thuê
            DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
            DichVuDaThueDAO dvtDAO = new DichVuDaThueDAO();
            for (int i = 0; i < model.getRowCount(); i++) {
                String maDV = model.getValueAt(i, 0).toString();
                String tenDV = model.getValueAt(i, 1).toString();
                double donGia = Double.parseDouble(model.getValueAt(i, 2).toString());
                double soLuong = Double.parseDouble(model.getValueAt(i, 3).toString());

                DichVuDaThue dvt = new DichVuDaThue();
                dvt.setMaDVDT("DVDT" + System.currentTimeMillis());
                dvt.setMaDP(maDP);
                dvt.setMaDV(maDV);
                dvt.setTenDV(tenDV);
                dvt.setDonGia(donGia);
                dvt.setSoLuong(soLuong);

                dvtDAO.insert(dvt);
            }

            JOptionPane.showMessageDialog(this, "Lưu thông tin thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
        }
    }

