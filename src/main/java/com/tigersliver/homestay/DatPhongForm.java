    private void themDV() {
        DichVu selectedDichVu = (DichVu) cboDichVu.getSelectedItem();
        if (selectedDichVu == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ!");
            return;
        }

        // Lấy model của bảng đã tạo sẵn
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();

        String maDichVu = selectedDichVu.getMaDV();
        String tenDichVu = selectedDichVu.getTenDV();
        double donGia = selectedDichVu.getDonGia();

        // Kiểm tra xem dịch vụ đã tồn tại trong bảng chưa
        for (int i = 0; i < model.getRowCount(); i++) {
            if (maDichVu.equals(model.getValueAt(i, 0))) { // Cột 0 là Mã Dịch Vụ
                int soLuong = (int) model.getValueAt(i, 3) + 1; // Cột 3 là Số Lượng
                model.setValueAt(soLuong, i, 3); // Cập nhật số lượng
                return; // Thoát vì đã cập nhật số lượng
            }
        }

        // Nếu chưa có, thêm mới vào bảng
        Object[] rowData = {maDichVu, tenDichVu, donGia, 1};
        model.addRow(rowData);
    }

    private void xoaDV() {
        int selectedRow = tblDichVu.getSelectedRow();
        if (selectedRow != -1) { // Kiểm tra xem có dòng nào được chọn
            DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
            model.removeRow(selectedRow); // Xóa dòng đã chọn
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ cần xóa!");
        }
    }
    
